"""Generate tab_lagsearch.tex: the synchronization-search ablation on the
pi0.5/LIBERO-10 `delay` attack, lag-zero vs the estimated global tau*, swept
across query budgets |G| in {1, 16, 32, 64}.

off-column  <- per_episode_scores_raw_L0  (LAG_SEARCH=0 baseline)
tau* column <- per_episode_scores_raw     (LAG_SEARCH=1 LAG_MODE=global default)

Both columns use the SAME aggregation machinery as the main robust table
(av.calibrate -> av.auc / av.auc_group / av.tpr_point), so the numbers are
directly comparable to tab_robust_detection. |G|<=64 is honest only once each
cell has >=~64 watermarked episodes (see the re-roll); a guard prints n_wm.

Usage: make_tab_lagsearch.py [--write]   (default: print numbers only)
"""
from __future__ import annotations
import sys, argparse
from pathlib import Path
import numpy as np
import pandas as pd

sys.path.insert(0, "results")
import analyze_verification as av  # noqa: E402

DATA = Path("/workspace/vla")
RAW = DATA / "attack_c_data" / "per_episode_scores_raw"        # global tau* (default)
L0 = DATA / "attack_c_data" / "per_episode_scores_raw_L0"      # lag-zero baseline
OUT = DATA / "paper" / "tab_lagsearch.tex"

DELAYS = ["1", "2", "3"]
GS = [1, 16, 32, 64]
STEM = "pi05_libero_10_partial_map_delay_{}"


def metrics(csv_path):
    """(n_wm, {G: (auc, tpr)}) from a per-episode score CSV via the shared
    calibration. AUC at G=1 is the per-episode wm-vs-false-key AUC; AUC/TPR at
    G>1 use the group-sum bootstrap (same as the robust table)."""
    df = pd.read_csv(csv_path)
    cal = av.calibrate(df)
    n_wm = int(len(cal.z_h1))
    rng_a = np.random.default_rng(av.RNG_SEED + 1)
    rng_t = np.random.default_rng(av.RNG_SEED)
    out = {}
    for G in GS:
        if G == 1:
            auc = av.auc(cal.z_h1, cal.z_null.ravel())
            tpr = av.tpr_point(cal.z_h1, cal.z_null, 1, av.FPR_MAIN, rng_t)
        else:
            auc = av.auc_group(cal.z_h1, cal.z_null, G, rng_a)
            tpr = av.tpr_point(cal.z_h1, cal.z_null, G, av.FPR_MAIN, rng_t)
        out[G] = (float(auc), float(tpr))
    return n_wm, out


def collect():
    rows = []
    for d in DELAYS:
        stem = STEM.format(d)
        n0, off = metrics(L0 / f"{stem}.csv")
        n1, on = metrics(RAW / f"{stem}.csv")
        rows.append(dict(delay=d, n_off=n0, n_on=n1, off=off, on=on))
    return rows


def fmt(x):
    return f"{x:.2f}"


def render(rows):
    L = []
    L.append("% generated by results/make_tab_lagsearch.py "
             "(off=per_episode_scores_raw_L0, tau*=per_episode_scores_raw global default)")
    L.append(r"\begin{table}[t]\centering")
    L.append(r"\caption{Synchronization-search ablation on the \texttt{delay} attack, "
             r"$\pi_{0.5}$/LIBERO-10. Rows are delay length; columns sweep the query "
             r"budget $|G|$. Each entry is TPR at $1\%$ FPR (AUC in the $|G|{=}1$ column), "
             r"with the lag search off (lag-zero) and on (the estimated global $\tau^*$). "
             r"A constant delay is a pure time shift the global search marginalizes, so "
             r"$\tau^*$ recovers it; the residual at longer delay is graceful attenuation "
             r"that aggregation closes. $\pi_{0.5}$/RoboTwin and every non-delay condition "
             r"estimate $\tau^*{=}0$ and are unchanged by the search (omitted).}")
    L.append(r"\label{tab:lagsearch}\small")
    L.append(r"\begin{tabular}{c cccc c cccc}")
    L.append(r"\toprule")
    L.append(r" & \multicolumn{4}{c}{lag-zero} & \multicolumn{4}{c}{global $\tau^*$ search} \\")
    L.append(r"\cmidrule(lr){2-5}\cmidrule(lr){6-9}")
    L.append(r"delay & AUC & \multicolumn{3}{c}{TPR @ $|G|$} "
             r"& AUC & \multicolumn{3}{c}{TPR @ $|G|$} \\")
    L.append(r" & ($|G|{=}1$) & 16 & 32 & 64 & ($|G|{=}1$) & 16 & 32 & 64 \\")
    L.append(r"\midrule")
    TPR_GS = [16, 32, 64]
    for r in rows:
        off, on = r["off"], r["on"]
        cells = [r["delay"], fmt(off[1][0])] + [fmt(off[g][1]) for g in TPR_GS] \
            + [fmt(on[1][0])] + [fmt(on[g][1]) for g in TPR_GS]
        L.append(" & ".join(cells) + r" \\")
    L.append(r"\bottomrule")
    L.append(r"\end{tabular}")
    L.append(r"\end{table}")
    return "\n".join(L) + "\n"


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--write", action="store_true")
    args = ap.parse_args()
    rows = collect()
    print(f"{'delay':>5} {'n_wm(off/on)':>14}   lag-zero AUC1/TPR16/32/64        "
          f"tau* AUC1/TPR16/32/64")
    for r in rows:
        off, on = r["off"], r["on"]
        flag = "" if min(r["n_off"], r["n_on"]) >= 64 else "  <-- n_wm<64, |G|=64 NOT honest yet"
        nwm = f'{r["n_off"]}/{r["n_on"]}'
        print(f"{r['delay']:>5} {nwm:>14}   "
              f"{off[1][0]:.2f} / {off[16][1]:.2f} {off[32][1]:.2f} {off[64][1]:.2f}      "
              f"{on[1][0]:.2f} / {on[16][1]:.2f} {on[32][1]:.2f} {on[64][1]:.2f}{flag}")
    if args.write:
        OUT.write_text(render(rows))
        print(f"\nWROTE {OUT}")
    else:
        print("\n(dry run; pass --write to emit tab_lagsearch.tex)")
        print(render(rows))


if __name__ == "__main__":
    main()
