#!/usr/bin/env python3
"""tab:ablation -- whitened WMF vs. raw matched filter, clean, all 4 cells.

For each cell, from the SAME cached re-scored pool (cache_*.csv), score both
ways and report the metric each detection block governs:
  AUC      detection AUC, |G|=1                      (av.calibrate + av.auc)
  R1       closed-set identification rank-1, |G|=1   (ai.calibrate + ai.cmc_curve)
  FPR      realized plain-FPR at nominal 1%, |G|=16  (negative control operating pt)

Whitened reproduces tab:main (AUC) and tab:identification (R1); the gate prints
the whitened R1 next to the committed tab:identification value. The ablation:
removing the whitening RAISES separation (AUC, R1) but SACRIFICES FPR
conservativeness on the hardest cell (LingBot/RoboTwin) -- the whitening's real
job is keeping reported detection rates honest lower bounds.
"""
from __future__ import annotations
import sys
from pathlib import Path
import numpy as np
import pandas as pd

HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(HERE))
import analyze_verification as av
import analyze_identification as ai

J = 32
G_FPR, Q, NT = 16, 0.01, 20000
SCORE_DIR = HERE.parent / "attack_c_data" / "per_episode_scores"

# (model, dataset, cache, committed CSV, tab:identification R1 of record)
CELLS = [
    ("LingBot",      "LIBERO-10", "cache_lingbot_libero.csv", "lingbot_libero_10_partial_map_clean.csv", 0.61),
    ("LingBot",      "RoboTwin",  "cache_lingbot_rt.csv",     "lingbot_robotwin_partial_map_clean.csv",  0.06),
    (r"$\pi_{0.5}$",  "LIBERO-10", "cache_pi05_libero.csv",    "pi05_libero_10_partial_map_clean.csv",    0.28),
    (r"$\pi_{0.5}$",  "RoboTwin",  "cache_pi05_rt.csv",        "pi05_robotwin10_partial_map_clean.csv",   0.11),
]


def _df_whitened(csv):
    """Whitened scores from the committed CSV (paper data of record)."""
    return pd.read_csv(SCORE_DIR / csv)


def _df_raw(cache):
    """Raw scores from the re-scored cache."""
    df = pd.read_csv(HERE / cache)
    out = pd.DataFrame({"episode_id": [f"e{i}" for i in range(len(df))],
                        "variant": df["variant"], "s_true": df["st_r"]})
    for i in range(J):
        out[f"s_false_{i+1}"] = df[f"sfr_{i+1}"]
    return out


def _metrics(df):
    calv = av.calibrate(df)
    auc1 = av.auc(calv.z_h1, calv.z_null.ravel())
    cali = ai.calibrate(df)
    cmc, _, _, _ = ai.cmc_curve(cali, 1, np.random.default_rng(0))
    r1 = float(cmc[0])
    # realized plain FPR at |G|=16, nominal 1%
    z_h0, z_null = calv.z_h0_plain, calv.z_null
    if len(z_h0) == 0:
        fpr = float("nan")
    else:
        rng = np.random.default_rng(0)
        ep = rng.integers(0, z_null.shape[0], size=(NT, G_FPR)); key = rng.integers(0, J, size=(NT, G_FPR))
        thr = np.quantile(z_null[ep, key].sum(1), 1 - Q)
        fpr = float((z_h0[rng.integers(0, len(z_h0), size=(NT, G_FPR))].sum(1) >= thr).mean())
    return auc1, r1, fpr


def main():
    print(f"{'cell':22s}{'scorer':9s}{'AUC':>7s}{'R1':>7s}{'FPR16':>8s}   gate(whit R1 vs tab:ident)")
    rows = {}
    for model, ds, cache, csv, paper_r1 in CELLS:
        w = _metrics(_df_whitened(csv)); r = _metrics(_df_raw(cache))
        rows[(model, ds)] = (w, r)
        name = f"{model}/{ds}".replace(r"$\pi_{0.5}$", "pi0.5")
        stale = "  <-- STALE in tab:ident" if abs(w[1] - paper_r1) > 0.05 else ""
        print(f"{name:22s}{'whitened':9s}{w[0]:7.3f}{w[1]:7.3f}{w[2]:8.3f}   tab:id={paper_r1:.2f} d={abs(w[1]-paper_r1):.2f}{stale}")
        print(f"{'':22s}{'raw':9s}{r[0]:7.3f}{r[1]:7.3f}{r[2]:8.3f}")
    # --- LaTeX ---
    def cells(m, hl_fpr=False):
        a, r1, f = m
        fs = f"\\textbf{{{f:.3f}}}" if hl_fpr else f"{f:.3f}"
        return f"{a:.2f} & {r1:.2f} & {fs}"
    body = []
    for model, ds, _c, _csv, _r1 in CELLS:
        w, r = rows[(model, ds)]
        # flag the one anti-conservative FPR (raw, |G|=16, > 1.5x nominal)
        hl = r[2] > Q * 1.5
        body.append(f"{model} & {ds} & {cells(w)} & {cells(r, hl_fpr=hl)} \\\\")
    tex = r"""% auto-generated by results/make_ablation_table.py -- do not hand-edit
\begin{table}[t]\centering
\caption{Verifier scoring ablation on the clean rollouts of \Cref{tab:main}. The
shipped detection score is the whitened matched filter $\psi(\hat z,r)=r^\top
\Sigma^{-1}\hat z$ of \Cref{eq:zscore}, whose noise covariance $\Sigma$ is
estimated from the false-key bank (rank-3 subspace); we ablate the whitening by
scoring with the raw matched filter $r^\top\hat z$ instead. AUC (detection,
$|G|{=}1$) and rank-1 (closed-set identification, $|G|{=}1$, the gallery of
\Cref{tab:identification}) measure separability; FPR is the realized
plain-rollout false-positive rate at the nominal $1\%$ operating point
($|G|{=}16$), the negative-control quantity of \Cref{fig:neg-control-h0}.
Removing the whitening raises separability on every cell, but on the hardest cell
(LingBot/RoboTwin) it raises the realized false-positive rate to $2.6\%$
(\textbf{bold}) where the whitened score holds at $0.2\%$: the whitening trades a
little separability for false-positive rates that stay at or below nominal, which
a provenance claim requires. Recovery (MAP vs.\ reverse-ODE) is ablated
separately in \Cref{sec:rationale-rob}.}
\label{tab:ablation}\small
\begin{tabular}{ll ccc ccc}
\toprule
 & & \multicolumn{3}{c}{Whitened WMF (shipped)} & \multicolumn{3}{c}{Raw MF ($-$whitening)} \\
\cmidrule(lr){3-5}\cmidrule(lr){6-8}
Model & Dataset & AUC & R1 & FPR & AUC & R1 & FPR \\
\midrule
""" + "\n".join(body) + r"""
\bottomrule
\end{tabular}
\end{table}
"""
    (HERE / "tab_ablation.tex").write_text(tex)
    print(f"\nwrote {HERE/'tab_ablation.tex'}")


if __name__ == "__main__":
    main()
